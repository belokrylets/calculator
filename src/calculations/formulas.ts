export function sum(a: number, b: number):number {
    return a + b;
}
export function subtraction(a: number, b: number):number {
    return a - b;
}
export function multiplication(a: number, b: number):number {
    return a * b;
}
export function division(a: number, b: number):number {
    return a / b;
}
export function root(a: number):number {
    return Math.sqrt(a);
}
export function percent(a: number):number {
    return a / 100;
}
export function sumPercent(a: number, b: number):number {
    return a + ((a * b) / 100);
}
export function subtractionPercent(a: number, b: number):number {
    return a - ((a * b) / 100);
}